def residuals(u=None, return_map=_DEFAULT_RETURN_MAP):
    """Decoupled multidisciplinary consistency residual r(u) = u_supplied -
    f(x, u), leaf decompose — the exact quantity evaluate_residual computes.

    Every inter-discipline feedback edge is severed: each consumer's coupling
    input is driven by the SUPPLIED guess of the coupling variable (the decisive
    line `state[COUPLING_INPUTS[full]]`), NOT by the producer's live output.

    u: optional {'disc.var': value} overriding RECORDED_STATE (a value on any
       promoted endpoint overrides all its siblings). Omitted entries fall back
       to the recorded value, then to the component default. return_map=True
       returns the discipline output maps f instead of the residual u_supplied-f.
    Returns (result, norm): result maps each coupling variable to its residual
    (or output-map) ndarray; norm is the L2 norm over the flattened stack.
    """
    state = dict(RECORDED_STATE)
    for _k, _v in (u or {}).items():
        for _sib in PROMOTION_SIBLINGS.get(_k, (_k,)):
            state[_sib] = _v
    prob = _PROB
    for _unit, _in_names in UNIT_INPUTS.items():
        for _name in _in_names:
            _full = f"{_unit}.{_name}"
            if _full in COUPLING_INPUTS:                 # severed: feed the guess
                prob.set_val(_full, np.asarray(state[COUPLING_INPUTS[_full]], dtype=float))
            elif _full in state:                         # x / shared input
                prob.set_val(_full, np.asarray(state[_full], dtype=float))
            # else: leave the component default (1.0), as the oracle does.
    prob.run_model()
    result = {}
    for _cv in COUPLING_VARS:
        _f = np.asarray(prob.get_val(_cv), dtype=float).flatten()
        if _cv not in state:
            raise KeyError(
                f"coupling variable {_cv!r} has no supplied (u) or recorded "
                f"value to subtract against — pass it in u.")
        _guess = np.asarray(state[_cv], dtype=float).flatten()
        result[_cv] = _f if return_map else (_guess - _f)
    _stack = (np.concatenate([result[k] for k in result]) if result
              else np.array([]))
    return result, float(np.linalg.norm(_stack))


